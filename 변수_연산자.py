a=5
b=3
x=input("연산자를 입력하시오:")

if x== "+":
    print("연산자: +")
    print("5", "+", "3","=",(a+b))
elif x== "-":
    print("연산자: -")
    print("5", "-", "3","=",(a-b))
elif x== "*":
    print("연산자: *")
    print("5", "*", "3","=",(a*b))
elif x== "/":
    print("연산자: /")
    print("5", "/", "3","=",(a/b))
